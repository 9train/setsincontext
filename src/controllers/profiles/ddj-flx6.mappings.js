function freezeBinding(binding) {
  const out = { ...binding };
  if (out.raw) out.raw = Object.freeze({ ...out.raw });
  if (out.context) out.context = Object.freeze({ ...out.context });
  if (out.activation) out.activation = Object.freeze({ ...out.activation });
  if (out.feel) {
    out.feel = Object.freeze({
      ...out.feel,
      dispatcher: Object.freeze({ ...(out.feel.dispatcher || {}) }),
    });
  }
  return Object.freeze(out);
}

function midiBinding({
  id,
  label,
  rawTarget,
  canonical,
  kind,
  channel,
  code,
  valueShape,
  feel,
  context,
  activation,
  note,
}) {
  return freezeBinding({
    id,
    label,
    rawTarget,
    canonical,
    context,
    activation,
    valueShape,
    feel,
    note,
    raw: {
      transport: 'midi',
      kind,
      channel,
      code,
      key: `${kind}:${channel}:${code}`,
    },
  });
}

function absolutePair({
  idBase,
  rawTarget,
  canonical,
  channel,
  primaryCode,
  secondaryCode,
  feelPrimary,
  feelSecondary,
  context,
  label,
}) {
  return [
    midiBinding({
      id: `${idBase}.primary`,
      rawTarget,
      canonical,
      kind: 'cc',
      channel,
      code: primaryCode,
      valueShape: 'absolute',
      feel: feelPrimary,
      context,
      note: `${label} primary CC lane captured in the repo's learned mappings.`,
    }),
    midiBinding({
      id: `${idBase}.secondary`,
      rawTarget,
      canonical,
      kind: 'cc',
      channel,
      code: secondaryCode,
      valueShape: 'absolute',
      feel: feelSecondary,
      context,
      note: `${label} secondary paired CC lane captured in the repo's learned mappings.`,
    }),
  ];
}

function buttonPair({
  idBase,
  rawTarget,
  canonical,
  channel,
  code,
  context,
  label,
  note,
}) {
  return [
    midiBinding({
      id: `${idBase}.press`,
      label,
      rawTarget,
      canonical,
      kind: 'noteon',
      channel,
      code,
      valueShape: 'binary',
      context,
      note: note || `${label} press lane from the saved FLX6 room map.`,
    }),
    midiBinding({
      id: `${idBase}.release`,
      label,
      rawTarget,
      canonical,
      kind: 'noteoff',
      channel,
      code,
      valueShape: 'binary',
      context,
      note: note || `${label} release lane from the saved FLX6 room map.`,
    }),
  ];
}

