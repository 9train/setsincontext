function buildPadModeBindings() {
  return flx6DeckPadLanes.flatMap((lane) =>
    flx6PadBanks.flatMap((bank) =>
      buttonPair({
        idBase: `deck.${lane.side}.pad_mode.${bank.mode}.${lane.deckLayer}${bank.shifted ? '.shifted' : ''}`,
        rawTarget: `${bank.modeTarget}_${lane.surface}`,
        canonical: `deck.${lane.side}.pad_mode.${bank.mode}`,
        channel: lane.buttonChannel,
        code: bank.modeButtonCode,
        context: bank.shifted
          ? { deckLayer: lane.deckLayer, shifted: true }
          : { deckLayer: lane.deckLayer },
        label: `${lane.label} ${bank.label} mode`,
      })
    )
  );
}

function buildPadSurfaceBindings() {
  // The saved FLX6 room map exposes one stable pad surface per slot while the
  // note bank changes with pad mode and deck layer. We promote those banks into
  // the controller layer as physical pads plus authored mode context, and leave
  // mode-specific app behavior above the controller layer.
  return flx6DeckPadLanes.flatMap((lane) =>
    flx6PadBanks.flatMap((bank) =>
      Array.from({ length: 8 }, (_, index) => {
        const slot = index + 1;
        return buttonPair({
          idBase: `deck.${lane.side}.pad.${slot}.${lane.deckLayer}.${bank.mode}`,
          rawTarget: `pad_${lane.surface}_${slot}`,
          canonical: `deck.${lane.side}.pad.${slot}`,
          channel: lane.padChannel,
          code: bank.padCodeStart + index,
          context: {
            deckLayer: lane.deckLayer,
            mode: bank.mode,
          },
          label: `${lane.label} pad ${slot} ${bank.label}`,
        });
      }).flat()
    )
  );
}

